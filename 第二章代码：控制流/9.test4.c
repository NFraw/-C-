#include<stdio.h>

//此份代码实现了不断地输入数据进行处理

int main(){
    int x;
    while(scanf("%d", &x) != EOF)   //当输入不是文件结束符时，可以一直输入
    {
        printf("2 * x = %d\n", 2 * x);  //输出x的两倍
    }
    return 0;
}